package com.fif.ws.model.master;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 *
 * @author RizkyAkbar
 */
@Entity
@Table(name = "public String getServOffIdByZip(String zip)")
public class ServiceOfficeZipModel {
    
    @Id
    private String SERV_OFFICE_CODE;
    private String DEALER_CODE;
    private String ZIP;

    public ServiceOfficeZipModel() {
    }

    public ServiceOfficeZipModel(String SERV_OFFICE_CODE, String DEALER_CODE, String ZIP) {
        this.SERV_OFFICE_CODE = SERV_OFFICE_CODE;
        this.DEALER_CODE = DEALER_CODE;
        this.ZIP = ZIP;
    }

    public String getSERV_OFFICE_CODE() {
        return SERV_OFFICE_CODE;
    }

    public void setSERV_OFFICE_CODE(String SERV_OFFICE_CODE) {
        this.SERV_OFFICE_CODE = SERV_OFFICE_CODE;
    }

    public String getDEALER_CODE() {
        return DEALER_CODE;
    }

    public void setDEALER_CODE(String DEALER_CODE) {
        this.DEALER_CODE = DEALER_CODE;
    }

    public String getZIP() {
        return ZIP;
    }

    public void setZIP(String ZIP) {
        this.ZIP = ZIP;
    }
    
}
