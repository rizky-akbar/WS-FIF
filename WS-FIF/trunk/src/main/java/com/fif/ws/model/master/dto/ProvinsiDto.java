package com.fif.ws.model.master.dto;

/**
 *
 * @author RizkyAkbar
 */
public class ProvinsiDto {
    
    private String provinsiCode  ;
    
    private String provinsiName  ;

    public ProvinsiDto() {
    }

    public String getProvinsiCode() {
        return provinsiCode;
    }

    public void setProvinsiCode(String provinsiCode) {
        this.provinsiCode = provinsiCode;
    }

    public String getProvinsiName() {
        return provinsiName;
    }

    public void setProvinsiName(String provinsiName) {
        this.provinsiName = provinsiName;
    }

    public ProvinsiDto(String provinsiCode, String provinsiName) {
        this.provinsiCode = provinsiCode;
        this.provinsiName = provinsiName;
    }

    
}
