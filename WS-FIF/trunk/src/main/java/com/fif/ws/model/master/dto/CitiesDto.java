package com.fif.ws.model.master.dto;

/**
 *
 * @author RizkyAkbar
 */
public class CitiesDto {

    private String cityCode;
    private String cityName;
    private String dati2;
    private String provinsiCode;

    public CitiesDto() {
    }

    public String getCityCode() {
        return cityCode;
    }

    public void setCityCode(String cityCode) {
        this.cityCode = cityCode;
    }

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public String getDati2() {
        return dati2;
    }

    public void setDati2(String dati2) {
        this.dati2 = dati2;
    }

    public String getProvinsiCode() {
        return provinsiCode;
    }

    public void setProvinsiCode(String provinsiCode) {
        this.provinsiCode = provinsiCode;
    }

    public CitiesDto(String cityCode, String cityName, String dati2, String provinsiCode) {
        this.cityCode = cityCode;
        this.cityName = cityName;
        this.dati2 = dati2;
        this.provinsiCode = provinsiCode;
    }

}
