package com.fif.ws.model.master;

import java.util.Date;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.Temporal;

/**
 *
 * @author RizkyAkbar
 */
@Entity
@Table(name = "FS_MST_EDUCATION")
public class EducationModel {
    @Id
    private String EDU_TYPE;
    private String EDU_DESCR;
    private String BI_STATUS;
    private String AAB_CODE;
    @Temporal(javax.persistence.TemporalType.DATE)
    private Date CREATE_DATE;
    @Temporal(javax.persistence.TemporalType.DATE)
    private Date UPDATE_DATE;
    /**
     * @return the EDU_TYPE
     */
    public String getEDU_TYPE() {
        return EDU_TYPE;
    }

    /**
     * @param EDU_TYPE the EDU_TYPE to set
     */
    public void setEDU_TYPE(String EDU_TYPE) {
        this.EDU_TYPE = EDU_TYPE;
    }

    /**
     * @return the EDU_DESCR
     */
    public String getEDU_DESCR() {
        return EDU_DESCR;
    }

    /**
     * @param EDU_DESCR the EDU_DESCR to set
     */
    public void setEDU_DESCR(String EDU_DESCR) {
        this.EDU_DESCR = EDU_DESCR;
    }

    /**
     * @return the BI_STATUS
     */
    public String getBI_STATUS() {
        return BI_STATUS;
    }

    /**
     * @param BI_STATUS the BI_STATUS to set
     */
    public void setBI_STATUS(String BI_STATUS) {
        this.BI_STATUS = BI_STATUS;
    }

    /**
     * @return the AAB_CODE
     */
    public String getAAB_CODE() {
        return AAB_CODE;
    }

    /**
     * @param AAB_CODE the AAB_CODE to set
     */
    public void setAAB_CODE(String AAB_CODE) {
        this.AAB_CODE = AAB_CODE;
    }

    /**
     * @return the CREATE_DATE
     */
    public Date getCREATE_DATE() {
        return CREATE_DATE;
    }

    /**
     * @param CREATE_DATE the CREATE_DATE to set
     */
    public void setCREATE_DATE(Date CREATE_DATE) {
        this.CREATE_DATE = CREATE_DATE;
    }

    /**
     * @return the UPDATE_DATE
     */
    public Date getUPDATE_DATE() {
        return UPDATE_DATE;
    }

    /**
     * @param UPDATE_DATE the UPDATE_DATE to set
     */
    public void setUPDATE_DATE(Date UPDATE_DATE) {
        this.UPDATE_DATE = UPDATE_DATE;
    }
    
}
