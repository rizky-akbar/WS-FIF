package com.fif.ws.model.dto.order;

/**
 *
 * @author RizkyAkbar
 */
public class OrderFullPromotions {

    private String promoMasterCode;
    private String promoDetailCode;

    public OrderFullPromotions() {
    }

    public OrderFullPromotions(String promoMasterCode, String promoDetailCode) {
        this.promoMasterCode = promoMasterCode;
        this.promoDetailCode = promoDetailCode;
    }

    public String getPromoMasterCode() {
        return promoMasterCode;
    }

    public void setPromoMasterCode(String promoMasterCode) {
        this.promoMasterCode = promoMasterCode;
    }

    public String getPromoDetailCode() {
        return promoDetailCode;
    }

    public void setPromoDetailCode(String promoDetailCode) {
        this.promoDetailCode = promoDetailCode;
    }

}
